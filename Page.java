public class Page {

    private int size;
    private String name;

    public Page(String name, int size) {
        this.size = size;
        this.name = name;
    }

	public int getSize() {
		return size;
	}

	public void setSize(int size) {
		this.size = size;
	}

	public String getName() {
		return name;
	}
	
	public void setName(String name)
	{
		this.name = name;
	}

	
    
    
    
    
   
}
